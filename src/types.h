#ifndef _TYPES_H_
#define _TYPES_H_

#include <stdint.h>

/* shorthand unsigned types */
typedef uint8_t  u8;
typedef uint16_t u16;
typedef uint32_t u32;
typedef uint64_t u64;

/* shorthand signed types */
typedef int8_t  s8;
typedef int16_t s16;
typedef int32_t s32;
typedef int64_t s64;

#endif /* _TYPES_H_ */
